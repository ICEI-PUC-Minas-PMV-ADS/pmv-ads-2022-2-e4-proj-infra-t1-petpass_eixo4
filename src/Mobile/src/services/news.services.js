import API from './webapi.services';
import { BASE_URL } from './urls';

export const getNews = async () => {
  try {
    return await API.get(`${BASE_URL}/News`).then(
      (response) => {
        return response.data;
      },
      (error) => {
        console.log(error);
        return null;
      }
    );
  } catch (error) {
    console.log(error);
    return null;
  }
};

